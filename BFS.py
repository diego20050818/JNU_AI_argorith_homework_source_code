from collections import deque

# deque 用法示例：
# 1. 创建双端队列：dq = deque()
# 2. 从右侧添加元素：dq.append(item)
# 3. 从左侧添加元素：dq.appendleft(item)
# 4. 从右侧移除元素：dq.pop()
# 5. 从左侧移除元素：dq.popleft()
# 6. 获取队列长度：len(dq)
# 7. 清空队列：dq.clear()
# 8. 旋转队列：dq.rotate(n)  # 正数 n 表示向右旋转，负数 n 表示向左旋转
# 9. 访问队列中的元素：dq[index]
# 10. 将列表转换为双端队列：dq = deque([1, 2, 3])

